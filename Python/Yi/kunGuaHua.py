#coding=utf-8
import turtle
import time


def draw_chuYaoYin_star_flag(times=20.0):
   
    width, height = 2*times, 1*times
    # 初始化屏幕和海龟
    window = turtle.Screen()
    aTurtle = turtle.Turtle()
    aTurtle.hideturtle()
    aTurtle.speed(10)
    # 画爻
    aTurtle.penup()
    aTurtle.goto(-300, -120)
    aTurtle.pendown()
    aTurtle.begin_fill()
    aTurtle.fillcolor('black')
    aTurtle.fd(width)
    aTurtle.right(90)
    aTurtle.fd(height)
    aTurtle.right(90)
    aTurtle.fd(width)
    aTurtle.right(90)
    aTurtle.fd(height)
    aTurtle.right(90)    
    aTurtle.end_fill()

    aTurtle.penup()
    aTurtle.goto(-240, -120)
    aTurtle.pendown()
    aTurtle.begin_fill()
    aTurtle.fillcolor('black')
    aTurtle.fd(width)
    aTurtle.right(90)
    aTurtle.fd(height)
    aTurtle.right(90)
    aTurtle.fd(width)
    aTurtle.right(90)
    aTurtle.fd(height)
    aTurtle.right(90)    
    aTurtle.end_fill()
  

 
if __name__ == '__main__':
    draw_chuYaoYin_star_flag()

